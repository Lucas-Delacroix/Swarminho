AXO_ART = r"""
        :++:                        .++:        
        *###=                      =####        
        =#==#*:  .:=*######*=:.  .*#==#+        
    .++. +#+.+#####-...  ...-#####+.+#+ .++:    
    =####+=##+*#=.             =##+##++####=    
    .*#=:=*####.                .####*=:=##.    
      :*##+=+#. .+#=.       =#+. .#*=+##*:      
    .=***####+. =##*.      .##*= .+####***+.    
    .*#+-:.:#+.  -=:        :=-  .+#:.:-+##.    
      :=*#*++#-    .*+=--=+*.    -#++*#*=:      
             -##:      ..      :##-             
               -*##*+======+*##*-               
                    :-=++=-:.                                                                
"""

BLUE = "\033[38;5;39m"
RESET = "\033[0m"


def print_banner() -> None:
    """Imprime o logo ASCII do Swarminho em azul e a mensagem inicial."""
    print(BLUE + AXO_ART + RESET)